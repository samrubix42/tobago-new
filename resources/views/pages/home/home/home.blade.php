    <div
        x-data="{
            items: @js($categories),
            active: 2,
            ticking: false,
            init() {
                this.$nextTick(() => {
                    this.scrollTo(this.active, false);
                    this.onScroll();
                    this.$refs.viewport.addEventListener('scroll', () => this.onScroll(), { passive: true });
                    window.addEventListener('resize', () => this.onScroll());
                });
            },
            cards() {
                return Array.from(this.$refs.viewport.querySelectorAll('[data-cat-card]'));
            },
            scrollTo(index, smooth = true) {
                const card = this.cards()[index];
                if (!card) return;
                card.scrollIntoView({ behavior: smooth ? 'smooth' : 'auto', inline: 'center', block: 'nearest' });
            },
            prev() {
                const nextIndex = (this.active - 1 + this.items.length) % this.items.length;
                this.active = nextIndex;
                this.scrollTo(nextIndex);
            },
            next() {
                const nextIndex = (this.active + 1) % this.items.length;
                this.active = nextIndex;
                this.scrollTo(nextIndex);
            },
            onScroll() {
                if (this.ticking) return;
                this.ticking = true;
                requestAnimationFrame(() => {
                    const viewport = this.$refs.viewport;
                    const cards = this.cards();
                    if (!viewport || cards.length === 0) {
                        this.ticking = false;
                        return;
                    }

                    const center = viewport.scrollLeft + (viewport.clientWidth / 2);
                    let bestIndex = 0;
                    let bestDistance = Infinity;

                    cards.forEach((card, index) => {
                        const cardCenter = card.offsetLeft + (card.offsetWidth / 2);
                        const distance = Math.abs(center - cardCenter);
                        if (distance < bestDistance) {
                            bestDistance = distance;
                            bestIndex = index;
                        }
                    });

                    this.active = bestIndex;
                    this.ticking = false;
                });
            },
        }"
